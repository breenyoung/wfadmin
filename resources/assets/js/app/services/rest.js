(function(){
    "use strict";

    angular.module("app.services").factory('RestService', ['$auth', 'Restangular', function($auth, Restangular){

        var baseProducts = Restangular.all('product');

        return {

            getAllProducts: function(scope)
            {
                baseProducts.getList().then(function(data)
                {
                    //console.log(data);
                    scope.products = data;
                });
            }
        };
    }]);
})();